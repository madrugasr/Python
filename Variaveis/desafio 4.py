n = input("\nDigite qualquer coisa: ")
print("\nÉ numerico?",n.isnumeric()) #é numerico?

print("\nÉ alfanumérico, letra?",n.isalpha()) #é alfanumérico, letra?

print("\nÉ alfanumérico e numerico?",n.isalnum()) #é alfanumérico e numerico?

print("\nEstá somente em letras maiusculas?",n.isupper()) #está somente em letras maiusculas?

print("\nEstá somente em letras minusculas?",n.islower()) #está somente em letras minusculas?

print("\nPode ser impresso?",n.isprintable()) #pode ser impresso?

print("\nTem espaço?",n.isspace()) #tem espaço?
print("\n")
